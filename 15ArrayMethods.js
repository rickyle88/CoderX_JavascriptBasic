// splice

let a = [1,3,2];
let b = [10,20];
var d = 4;

let c = a.concat(b);
console.log(c);

// push, return new array's length
a.push(d);
console.log(a);

// pop: removed last element,  return value of element removed
a.pop();
console.log(a);

// shift: removed first element, return value of element removed
a.shift();
console.log(a);

// unshift: add new element before first index, return new array's length
let e = 6;
a.unshift(e);
console.log(a);

